package cn.itcast.entity;

public class Theory {
	private int musicTheory_id;
	private String musicTheory_name;
	private String musicTheory_content;
	private int musicTheory_host;
	public int getMusicTheory_id() {
		return musicTheory_id;
	}
	public void setMusicTheory_id(int musicTheory_id) {
		this.musicTheory_id = musicTheory_id;
	}
	public String getMusicTheory_name() {
		return musicTheory_name;
	}
	public void setMusicTheory_name(String musicTheory_name) {
		this.musicTheory_name = musicTheory_name;
	}
	public String getMusicTheory_content() {
		return musicTheory_content;
	}
	public void setMusicTheory_content(String musicTheory_content) {
		this.musicTheory_content = musicTheory_content;
	}
	public int getMusicTheory_host() {
		return musicTheory_host;
	}
	public void setMusicTheory_host(int musicTheory_host) {
		this.musicTheory_host = musicTheory_host;
	}


}
