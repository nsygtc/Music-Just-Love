package cn.itcast.entity;

public class Video {
	 private  int video_id;
	 private  String video_name;
	 private  int video_type;  
	 private  int video_host; 
	 private  int video_score;
	public int getVideo_id() {
		return video_id;
	}
	public void setVideo_id(int video_id) {
		this.video_id = video_id;
	}
	public String getVideo_name() {
		return video_name;
	}
	public void setVideo_name(String video_name) {
		this.video_name = video_name;
	}
	public int getVideo_type() {
		return video_type;
	}
	public void setVideo_type(int video_type) {
		this.video_type = video_type;
	}
	public int getVideo_host() {
		return video_host;
	}
	public void setVideo_host(int video_host) {
		this.video_host = video_host;
	}
	public int getVideo_score() {
		return video_score;
	}
	public void setVideo_score(int video_score) {
		this.video_score = video_score;
	}

	 

}
